// Using a while loop
var sum = 0;
var number = 1;
while (number <= 50) {
  sum += number;
  number++;
}
console.log('Sum = ' + sum);

// Using a for loop.
var nums = [100, 300, 400, 60, 40];
var somme = 0;

for (var i = 0; i < nums.length; i++) {
  somme += nums[i];
}

console.log(somme);

